const add = function (num1, num2) {
  return num1 + num2;
};
const subract = function (num1, num2) {
  return num1 - num2;
};
const multiply = function (num1, num2) {
  return num1 * num2;
};
const divide = function (num1, num2) {
  return num1 / num2;
};

export { add, subract, multiply, divide };
